class Product {
  constructor(id, title, ownerId, price, quantity, size, time, code, docTitle) {
    this.id = id;
    this.title = title;
    this.ownerId = ownerId;
    this.price = price;
    this.quantity = quantity;
    this.size = size;
    this.time = time;
    this.code = code;
    this.docTitle = docTitle;
  }
}

export default Product;
